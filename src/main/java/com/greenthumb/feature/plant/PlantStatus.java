package com.greenthumb.feature.plant;

/**
 * Represents the current health status of a user's plant.
 *
 * @author Hamza Ali
 */
public enum PlantStatus {

    /** Plant is thriving and well cared for. */
    HEALTHY,

    /** Plant is showing signs of stress or neglect. */
    STRUGGLING,

    /** Plant has died — kept for historical care log reference. */
    DEAD
}
