package com.greenthumb.feature.care;

/**
 * Represents the type of care action performed on a plant.
 *
 * @author Hamza Ali
 */
public enum CareType {

    /** Watering the plant. */
    WATER,

    /** Applying fertiliser. */
    FERTILISE,

    /** Repotting into a larger container. */
    REPOT,

    /** Pruning dead or overgrown leaves. */
    PRUNE,

    /** Misting the plant's leaves. */
    MIST
}
